<template>
  <div class="mt-switch" :class="disabled ? 'is-disabled' : ''">
    <label>
      <span class="mt-switch__text"><slot>{{text}}</slot></span>
      <input type="checkbox"
             v-model="checked"
             :true-value="onValue"
             :false-value="offValue"
             @change="handleChange"
             :disabled="disabled">
      <div class="mt-switch__lever" :style="{ backgroundColor: checked ? onLeverColor : offLeverColor }">
        <span v-show="!checked" class="mt-switch__off" :style="{ color: offColor, backgroundColor: offBgColor }">{{offText}}</span>
        <span v-show="checked" class="mt-switch__on" :style="{ color: onColor, backgroundColor: onBgColor }">{{onText}}</span>
      </div>
    </label>
  </div>
</template>

<script>
  export default {
    name: 'MtSwitch',
    props: {
      value: {
        type: Boolean,
        default: false
      },
      onText: {
        type: String,
        default: '开'
      },
      offText: {
        type: String,
        default: '关'
      },
      onLeverColor: {
        type: String,
        default: ''
      },
      offLeverColor: {
        type: String,
        default: ''
      },
      onBgColor: {
        type: String,
        default: ''
      },
      offBgColor: {
        type: String,
        default: ''
      },
      onColor: {
        type: String,
        default: ''
      },
      offColor: {
        type: String,
        default: ''
      },
      onValue: {
        type: [Boolean, Number, String],
        default: true
      },
      offValue: {
        type: [Boolean, Number, String],
        default: false
      },
      disabled: {
        type: Boolean,
        default: false
      },
      text: String
    },

    data () {
      return {
        checked: this.value
      }
    },

    watch: {
      value (val) {
        this.checked = val
      }
    },

    methods: {
      handleChange (event) {
        const value = this.checked ? this.onValue : this.offValue
        this.$emit('input', value)
        this.$emit('change', value)
      }
    }
  }
</script>

